package hello;

public class Dataget {
	private String data;
	private String time;
	
	
	public Dataget(String data, String time) {
		super();
		this.data = data;
		this.time = time;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}

	

}
